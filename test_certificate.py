    # –î–∞—Ç–∞ –ø–æ —Ü–µ–Ω—Ç—Ä—É –Ω–∏–∂–Ω–µ–≥–æ –±–ª–æ–∫–∞ (–∏–ª–∏ —á—É—Ç—å –ª–µ–≤–µ–µ –µ—Å–ª–∏ –µ—Å—Ç—å –Ω–∞–¥–ø–∏—Å—å "–î–∞—Ç–∞ –≤—ã–¥–∞—á–∏:")
    # –Ø –≤–∏–∂—É —á—Ç–æ –Ω–∞ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–µ –µ—Å—Ç—å —Ç–µ–∫—Å—Ç "–î–∞—Ç–∞ –≤—ã–¥–∞—á–∏:", –ø–æ—ç—Ç–æ–º—É –¥–∞—Ç–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å —á—É—Ç—å –ø—Ä–∞–≤–µ–µ
