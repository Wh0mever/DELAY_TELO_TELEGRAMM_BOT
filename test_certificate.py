    # === –¶–í–ï–¢–ê ===
    # –ö—Ä–∞—Å–Ω—ã–π —Ü–≤–µ—Ç –¥–ª—è –∏–º–µ–Ω–∏ (—è—Ä–∫–∏–π —Ä–æ–∑–æ–≤–æ-–∫—Ä–∞—Å–Ω—ã–π –∫–∞–∫ –Ω–∞ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–µ)
    color_name = (226, 65, 107)  # #E2416B - —Ü–≤–µ—Ç –∑–∞–≥–æ–ª–æ–≤–∫–∞ "–°–ï–†–¢–ò–§–ò–ö–ê–¢"
    
    # –ß–µ—Ä–Ω—ã–π —Ü–≤–µ—Ç –¥–ª—è –¥–∞—Ç—ã
